  const relatedEvents = useQuery(
    api.events?.getEvents || 'skip',
    eventDetail?.event.city
      ? {
          page: 0,
          pageSize: 4,
          city: eventDetail.event.city,
          saleStatus: 'on_sale' as const,
        }
      : 'skip'
  )

